class Users::RegistrationsController < Devise::RegistrationsController
  before_action :configure_sign_up_params, only: [:create]
  before_action :configure_account_update_params, only: [:update]
  invisible_captcha only: :create

  def new
    build_resource {}
    self.resource.contact = Contact.new
    respond_with self.resource
  end

  def configure_sign_up_params
    role = Role.user_role.first
    devise_parameter_sanitizer.permit(:sign_up) {|u| u.permit(
      :email, :password, :password_confirmation,
      contact_attributes: [:name, :phone, :gender, :address]
    ).reverse_merge({ role_id: role.id })}
  end

  def configure_account_update_params
    devise_parameter_sanitizer.permit(:account_update) {|u| u.permit :password, :password_confirmation}
  end


